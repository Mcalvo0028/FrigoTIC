<h3><i class="fas fa-home"></i> Bienvenido a FrigoTIC</h3>
<p>FrigoTIC es una aplicación para gestionar el frigorífico compartido de la oficina. Aquí podrás apuntar los productos que consumes y ver tu historial.</p>

<h3><i class="fas fa-shopping-cart"></i> Pestaña Productos</h3>
<p>En esta sección podrás:</p>
<ul>
    <li><strong>Ver productos disponibles:</strong> Consulta todos los productos del frigorífico con sus precios.</li>
    <li><strong>Apuntar consumos:</strong> Haz clic en "Coger" para registrar que has cogido un producto.</li>
    <li><strong>Ver stock:</strong> El indicador de stock te muestra cuántas unidades quedan.</li>
</ul>

<h3><i class="fas fa-history"></i> Pestaña Movimientos</h3>
<p>Aquí puedes consultar:</p>
<ul>
    <li><strong>Historial de consumos:</strong> Todos los productos que has cogido.</li>
    <li><strong>Pagos realizados:</strong> Los pagos que has realizado al administrador.</li>
    <li><strong>Filtros:</strong> Filtra por fecha, producto o tipo de movimiento.</li>
    <li><strong>Deuda actual:</strong> Consulta cuánto debes actualmente.</li>
</ul>

<h3><i class="fas fa-user-cog"></i> Pestaña Perfil</h3>
<p>Gestiona tu cuenta:</p>
<ul>
    <li><strong>Cambiar contraseña:</strong> Actualiza tu contraseña de acceso.</li>
    <li><strong>Modificar email:</strong> Actualiza tu correo electrónico.</li>
    <li><strong>Ver resumen:</strong> Consulta un resumen de tu actividad.</li>
</ul>

<h3><i class="fas fa-euro-sign"></i> Pagos</h3>
<p>Una vez al mes, deberás pagar la deuda acumulada al administrador. Él registrará el pago y tu deuda volverá a cero.</p>

<h3><i class="fas fa-question-circle"></i> ¿Necesitas más ayuda?</h3>
<p>Contacta con el administrador si tienes alguna duda o problema con la aplicación.</p>
