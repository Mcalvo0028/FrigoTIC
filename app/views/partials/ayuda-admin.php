<h3><i class="fas fa-home"></i> Panel de Administración</h3>
<p>Como administrador de FrigoTIC, tienes acceso completo para gestionar el frigorífico compartido.</p>

<h3><i class="fas fa-users"></i> Pestaña Usuarios</h3>
<p>Gestión completa de usuarios:</p>
<ul>
    <li><strong>Ver usuarios:</strong> Lista de todos los usuarios registrados.</li>
    <li><strong>Crear usuario:</strong> Añade nuevos usuarios al sistema.</li>
    <li><strong>Resetear contraseña:</strong> Restablece la contraseña de un usuario.</li>
    <li><strong>Ver deudas:</strong> Consulta la deuda de cada usuario.</li>
    <li><strong>Registrar pagos:</strong> Marca las deudas como pagadas.</li>
</ul>

<h3><i class="fas fa-box"></i> Pestaña Productos</h3>
<p>Gestión del inventario:</p>
<ul>
    <li><strong>Añadir productos:</strong> Crea nuevos productos con imagen, precio de compra y venta.</li>
    <li><strong>Modificar productos:</strong> Actualiza precios, stock o información.</li>
    <li><strong>Eliminar productos:</strong> Desactiva productos que ya no se venden.</li>
    <li><strong>Control de stock:</strong> Registra las reposiciones cuando compres más productos.</li>
</ul>

<h3><i class="fas fa-file-invoice"></i> Pestaña Facturas</h3>
<p>Gestión de facturas de compra:</p>
<ul>
    <li><strong>Subir facturas:</strong> Sube PDFs de las facturas de compra.</li>
    <li><strong>Descargar:</strong> Descarga facturas para consultar.</li>
    <li><strong>Eliminar:</strong> Elimina facturas antiguas.</li>
</ul>

<h3><i class="fas fa-exchange-alt"></i> Pestaña Movimientos</h3>
<p>Control de todos los movimientos:</p>
<ul>
    <li><strong>Consumos:</strong> Ver todos los productos cogidos por los usuarios.</li>
    <li><strong>Pagos:</strong> Historial de pagos recibidos.</li>
    <li><strong>Reposiciones:</strong> Registro de reposiciones de stock.</li>
    <li><strong>Filtros avanzados:</strong> Filtra por usuario, fecha, producto o tipo.</li>
</ul>

<h3><i class="fas fa-chart-bar"></i> Pestaña Gráficos</h3>
<p>Estadísticas visuales:</p>
<ul>
    <li><strong>Consumos por mes:</strong> Evolución temporal de consumos.</li>
    <li><strong>Productos más consumidos:</strong> Ranking de productos.</li>
    <li><strong>Consumos por usuario:</strong> Quién consume más.</li>
    <li><strong>Pagos vs consumos:</strong> Balance financiero.</li>
</ul>

<h3><i class="fas fa-cog"></i> Pestaña Configuración</h3>
<p>Ajustes del sistema:</p>
<ul>
    <li><strong>Cambiar contraseña:</strong> Actualiza tu contraseña de admin.</li>
    <li><strong>Configuración SMTP:</strong> Ajusta el servidor de correo.</li>
    <li><strong>Plantillas de correo:</strong> Personaliza los correos automáticos.</li>
    <li><strong>Base de datos:</strong> Configuración de conexión (avanzado).</li>
</ul>

